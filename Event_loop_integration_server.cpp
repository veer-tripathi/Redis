// Buffer management

struct Buffer{
    uint8_t *buf;
    size_t cap;
    size_t start;
    size_t end;
};

static void buf_init(Buffer &b,size_t cap=4096){
    b.buf=new uint8_t[cap];
    b.cap=cap;
    b.start=0;
    b.end=0;
}

static size_t buf_size(const Buffer& b){
    return b.end-b.start;
}

static uint8_t* buf_data(const Buffer& b){
    return b.buf+b.start;
}

static void buf_consume(Buffer &b, size_t n){
    b.start+=n;
    if(b.start==b.end){
        b.start=0;
        b.end=0;
    }
}

static void buf_ensure(Buffer& b, size_t need){
    size_t freeback=b.cap-b.end;
    size_t data_len=buf_size(b);

    if(freeback>=need){
        return;
    }
    if(b.start>0 && (b.cap-data_len)>=need){
        memmove(b.buf, b.buf + b.start, data_len);
        b.start = 0;
        b.end = data_len;
        return;
    }
    size_t new_cap=2*b.cap;
    while(new_cap<data_len+need){
        new_cap*=2;
    }
    uint8_t *nb=new uint8_t[new_cap];
    memcpy(nb, b.buf+b.start, data_len);
    delete[] b.buf;
    b.buf=nb;
    b.cap=new_cap;
    b.start=0;
    b.end=data_len;
}

static void buf_append(Buffer &b, const uint8_t *data, size_t len) {
    buf_ensure(b, len);
    memcpy(b.buf + b.end, data, len);
    b.end += len;
}



// Connection

static void msg(const char *msg) {
    fprintf(stderr, "%s\n", msg);
}

static void msg_errno(const char *msg) {
    fprintf(stderr, "[errno:%d] %s\n", errno, msg);
}

static void die(const char *msg) {
    fprintf(stderr, "[%d] %s\n", errno, msg);
    abort();
}

static void fd_set_nb(int fd) {
    errno = 0;
    int flags = fcntl(fd, F_GETFL, 0);
    if (errno) {
        die("fcntl error");
        return;
    }

    flags |= O_NONBLOCK;

    errno = 0;
    (void)fcntl(fd, F_SETFL, flags);
    if (errno) {
        die("fcntl error");
    }
}

const size_t k_max_msg = 32 << 20;  // likely larger than the kernel buffer

